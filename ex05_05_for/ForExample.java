package ex05_05_for;

public class ForExample {
	public static void main(String args[]) {
		int cnt;
		for (cnt =1; cnt <= 10; cnt ++) {
			System.out.println(cnt);
		}
		
		cnt = 0;
		System.out.println(cnt);
		
		
		System.out.println("³¡.==========================");
		
		for (cnt = 10 ; cnt >= 0 ; cnt--) {
			System.out.println(cnt);
		}
		
	}

}
